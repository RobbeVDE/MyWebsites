		 			<h2>Hoe verloopt onze actie?</h2>

		 			<p>De leerlingen zijn volop aan het repeteren voor verschillende huiskamerconcerten van ongeveer 30 min.  Ieder huiskamerconcert heeft een ander thema .
Wonen jullie in de buurt van Gooik dan klikken jullie door naar Gooik. Ligt Herne meer in jullie omgeving dan klikken jullie door naar Herne.
Jullie maken een keuze uit de verschillende thema’s en boeken via deze site een huiskamerconcert. ( wij contacteren jullie voor de verdere afspraken )
U nodigt vrienden en familie uit om het huiskamerconcert  bij te wonen en maakt er een gezellig momentje van . 
Per aanwezig persoon in de huiskamer wordt er een bijdrage van €3 gevraagd. Op die manier hopen wij van € 1000 te kunnen schenken aan Het Huispaleis.
Op maandag 21 december willen wij met alle leerlingen dit geld overhandigen en ons plaatje aanvragen.</p>

