	 			<h2>Waarom doen we dit?</h2>

	 			<p>Elk jaar organiseren de woordklassen Gooik en Herne hun optreden in een cultureel centrum. Een optreden in een professionele zaal met spots, techiek. Kortom alles erop en eraan. De keerzijde van de medaille is dat zo’n zaal huren wel wat geld kost. Veel geld. Geld die we eigenlijk zouden kunnen schenken aan een goed doel. Al jaren speelt het in ons hoofd om het geld die we besteden aan kadootjes tijdens ons kerstfeestje te schenken aan Music for life. Maar er werd nooit iets concreets ondernomen. Tot vandaag</p>

